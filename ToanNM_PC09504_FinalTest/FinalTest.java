    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        try {
            String kq = JOptionPane.showInputDialog(this, "Nhập tên Điện Thoại muốn tìm", "Tìm Kiếm", JOptionPane.QUESTION_MESSAGE);
            if (kq.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập Điện Thoại");
                kq = JOptionPane.showInputDialog(this, "Nhập tên Điện Thoại muốn tìm", "Tìm Kiếm", JOptionPane.QUESTION_MESSAGE);
            } else {
                try {
                    model.setRowCount(0);
                    KetNoiCSDL();
                    DecimalFormat df = new DecimalFormat("###,###");
                    String sql = "SELECT * FROM DIENTHOAI WHERE TENDIENTHOAI LIKE ?";
                    PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
                    cauLenh.setString(1, "%" + kq + "%");
                    ResultSet ketQua = cauLenh.executeQuery();
                    while (ketQua.next() == true) {
                        String tenDienThoai = ketQua.getString(1);
                        String hangSanXuat = ketQua.getString(2);
                        int soLuong = ketQua.getInt(3);
                        int donGia = ketQua.getInt(4);
                        int tongTien = soLuong * donGia;
                        String donGiaFormatted = df.format(donGia);
                        String tongTienFormatted = df.format(tongTien);
                        String HinhAnh = ketQua.getString(5);

                        model.addRow(new Object[]{tenDienThoai, hangSanXuat, soLuong, donGiaFormatted, tongTienFormatted, HinhAnh});
                    }
                    ketNoi.close();

                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(FinalTest.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(FinalTest.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

