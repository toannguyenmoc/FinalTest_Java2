    public void DoDuLieuLenBang() throws ClassNotFoundException, SQLException {
        model.setRowCount(0);
        KetNoiCSDL();
        String sql = "SELECT * FROM DIENTHOAI";
        PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
        DecimalFormat df = new DecimalFormat("###,###");
        ResultSet ketQua = cauLenh.executeQuery();
        while (ketQua.next() == true) {
            String tenDienThoai = ketQua.getString(1);
            String hangSanXuat = ketQua.getString(2);
            int soLuong = ketQua.getInt(3);

            int donGia = ketQua.getInt(4);
            int tongTien = soLuong * donGia;
            String donGiaFormatted = df.format(donGia);
            String tongTienFormatted = df.format(tongTien);
            String HinhAnh = ketQua.getString(5);

            model.addRow(new Object[]{tenDienThoai, hangSanXuat, soLuong, donGiaFormatted, tongTienFormatted, HinhAnh});
        }
        ketNoi.close();
    }

    public void DanhSachGiay() throws ClassNotFoundException, SQLException {
        int viTri = tblBang.getSelectedRow();
        DecimalFormat df = new DecimalFormat("###,###");
        txtTenDienThoai.setText(tblBang.getValueAt(viTri, 0).toString());
        cboHangSanXuat.setSelectedItem(tblBang.getValueAt(viTri, 1).toString());
        txtSoLuong.setText(tblBang.getValueAt(viTri, 2).toString());
        txtDonGia.setText(tblBang.getValueAt(viTri, 3).toString());
        txtThanhTien.setText(tblBang.getValueAt(viTri, 4).toString());

        KetNoiCSDL();
        String sql = "SELECT HinhAnh FROM DIENTHOAI WHERE TENDIENTHOAI = ? ";
        PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
        cauLenh.setString(1, tblBang.getValueAt(viTri, 0).toString());

        ResultSet ketQua = cauLenh.executeQuery();
        while (ketQua.next() == true) {
            duongDan = ketQua.getString(1);
            ImageIcon hinhanh = new ImageIcon(new ImageIcon(duongDan).getImage().getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), Image.SCALE_SMOOTH));
            lblHinhAnh.setIcon(hinhanh);
        }
        ketNoi.close();
    }

